"""
섹터별 밸류체인 템플릿.

각 섹터에 대해:
  - 종목 목록 (ValueChain 노드)
  - 공급 관계 (엣지 + 의존도)
  - 경쟁 관계

공급관계는 수동 정의가 필요한 부분이다 (DART 비정형 텍스트 자동화 불가).
새 섹터 추가 시 이 파일에 build_* 함수를 추가하면 된다.
"""
from __future__ import annotations
from core.graph import ValueChain, Company


def get_chain(sector: str) -> ValueChain | None:
    """섹터 이름으로 미리 정의된 밸류체인 반환. 없으면 None."""
    builders = {
        "반도체":  build_semiconductor,
        "AI/전력": build_ai_power,
        "배터리":  build_battery,
        "방산":    build_defense,
        "조선":    build_shipbuilding,
        "바이오":  build_bio,
        "2차전지": build_battery,  # 배터리와 동일 체인
    }
    fn = builders.get(sector)
    return fn() if fn else None


